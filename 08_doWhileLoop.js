console.log("========= print 1 to 5 using do while loop ===========");

var count = 1 // initialization
do {
console.log(count);
count++;   // update
    
} while (count<=5); // condition 

console.log("========= print 80 to 70 using do while loop ===========");

var count = 80;
do {
    console.log(count);
    count--;
} while (count>=70);